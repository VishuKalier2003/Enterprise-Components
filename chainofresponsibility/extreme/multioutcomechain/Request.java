package chainofresponsibility.extreme.multioutcomechain;

public class Request {
    private String input;
    private int sum;

    public void setInput(String input) {
        this.input = input;
    }
    public String getInput() {
        return input;
    }

    public void setSum(int sum) {
        this.sum = sum;
    }
    public int getSum() {
        return sum;
    }
}
